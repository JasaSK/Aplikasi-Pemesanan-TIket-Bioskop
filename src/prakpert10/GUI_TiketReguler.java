    public void insert() {
        String judul = txtJudul.getText();
        int tempatDuduk = Integer.parseInt(txtTempatDuduk.getText());
        double jumlah = Double.parseDouble(txtJumlah.getText());
        double harga = Double.parseDouble(txtHarga.getText());

        double totalHarga = jumlah * harga;

        double diskon;
        if (jumlah > 5) {
            diskon = totalHarga * 0.30;
        } else if (jumlah > 3) {
            diskon = totalHarga * 0.20;
        } else {
            diskon = 0;
        }

        double totalSetelahDiskon = totalHarga - diskon;

        int index = cbMetodePembayaran.getSelectedIndex();
        String metode = (index == 0) ? "Kartu Kredit" : "Tunai";

        double bayar = Double.parseDouble(txtJumlahPembayaran.getText());

        String keterangan = (bayar > totalSetelahDiskon) ? "Pembayaran berhasil" : "Pembayaran gagal";

        try {
            koneksi();
            Statement statement = conn.createStatement();
            String sql = "INSERT INTO transaksiregular (judul_film, jumlah_tiket, harga_per_tiket, total_harga, diskon, metode_pembayaran, jumlah_bayar, keterangan, nomor_tempat_duduk, created_at) "
                    + "VALUES ('" + judul + "', " + jumlah + ", " + harga + ", " + totalHarga + ", " + totalSetelahDiskon + ", '" + metode + "', " + bayar + ", '" + keterangan + "', '" + tempatDuduk + "', CURRENT_TIMESTAMP)";
            statement.executeUpdate(sql);
            statement.close();
            JOptionPane.showMessageDialog(null, "Berhasil Memasukkan Data!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Terjadi Kesalahan Input: " + e.getMessage());
        }
        refresh();
    }

    public void update(int id) {
        String judul = txtJudul.getText();
        double jumlah = Double.parseDouble(txtJumlah.getText());
        double tempatDuduk = Double.parseDouble(txtTempatDuduk.getText());
        double harga = Double.parseDouble(txtHarga.getText());

        double totalHarga = jumlah * harga;

        double diskon;
        if (jumlah > 5) {
            diskon = totalHarga * 0.30;
        } else if (jumlah > 3) {
            diskon = totalHarga * 0.20;
        } else {
            diskon = 0;
        }

        double totalSetelahDiskon = totalHarga - diskon;

        int index = cbMetodePembayaran.getSelectedIndex();
        String metode = (index == 0) ? "Kartu Kredit" : "Tunai";

        double bayar = Double.parseDouble(txtJumlahPembayaran.getText());

        String keterangan = (bayar > totalSetelahDiskon) ? "Pembayaran berhasil" : "Pembayaran gagal";

        try {
            koneksi();
            Statement statement = conn.createStatement();
            String sql = "UPDATE transaksiregular SET "
                    + "judul_film = '" + judul + "', "
                    + "jumlah_tiket = " + jumlah + ", "
                    + "harga_per_tiket = " + harga + ", "
                    + "total_harga = " + totalHarga + ", "
                    + "diskon = " + totalSetelahDiskon + ", "
                    + "metode_pembayaran = '" + metode + "', "
                    + "jumlah_bayar = " + bayar + ", "
                    + "keterangan = '" + keterangan + "', "
                    + "nomor_tempat_duduk = '" + tempatDuduk + "' "
                    + "WHERE id = " + id;
            statement.executeUpdate(sql);
            statement.close();
            conn.close();
            JOptionPane.showMessageDialog(null, "Update Data Berhasil!");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error: " + e.getMessage());
        }
        refresh();
    }

    public void delete(int id) {
        int ok = JOptionPane.showConfirmDialog(null, "Apakah Anda yakin akan menghapus data ?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (ok == 0) {
            try {
                koneksi();
                String sql = "DELETE FROM transaksiregular WHERE id = " + id;
                java.sql.PreparedStatement stmt = conn.prepareStatement(sql);
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data Berhasil di hapus");
                batal();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Data gagal di hapus" + e.getMessage());
            }
        }
        refresh();
    }
