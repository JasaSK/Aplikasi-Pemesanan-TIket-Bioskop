    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
    // TODO add your handling code here:
    int selectedRow = tabelBioskop.getSelectedRow();
    if (selectedRow != -1) { // -1 berarti tidak ada yang dipilih
        DefaultTableModel model = (DefaultTableModel) tabelBioskop.getModel();
        model.removeRow(selectedRow);
    } else {
        JOptionPane.showMessageDialog(this, "Pilih baris yang ingin dihapus dulu!");
    }

    }//GEN-LAST:event_btnDeleteActionPerformed

