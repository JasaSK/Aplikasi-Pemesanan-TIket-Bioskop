    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        memoBioskop.setText("");
        TiketVIP TV = new TiketVIP();
        TV.judulFilm = txtJudul.getText();
        TV.tempatDuduk = txtTempatDuduk.getText();
        TV.jumlah = Integer.parseInt(txtJumlah.getText());
        TV.harga = Integer.parseInt(txtHarga.getText());

        memoBioskop.append("TIKET VIP\n");
        memoBioskop.append("Judul FIlm :" + TV.judulFilm + "\n");
        memoBioskop.append("Jumlah Tiket :" + TV.jumlah + "\n");
        memoBioskop.append("Nomor tempat duduk :" + TV.tempatDuduk + "\n");
        memoBioskop.append("Tanggal Pesan :" + TV.TanggalPembelian + "\n");
        memoBioskop.append("Total yang harus dibayar : Rp." + TV.totalHarga() + "\n");
        memoBioskop.append("Anda adalah VIP,total harga yang dibayar untuk VIP : Rp." + TV.BiayaTambahan() + "\n");
        if(TV.jumlah >= 5){
            memoBioskop.append("Anda membeli lebih dari 5 Tiket (Diskon 20%) \nJadi Jumlah yang harus dibayar : Rp." + TV.VipDiskon() + "\n");
        }
        else if(TV.jumlah >= 3){
            memoBioskop.append("Anda membeli lebih dari 3 TIket (Diskon 10%) \nJadi Jumlah yang harus dibayar : Rp." + TV.VipDiskon() + "\n");
        }
        else {
            memoBioskop.append("Anda tidak mendapatkan diskon");
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

