    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        
        TiketVIP TR = new TiketVIP();
        TR.setJudulFilm(txtJudul.getText());
        TR.setJumlah(Integer.parseInt(txtJumlah.getText()));
        TR.setHarga(Integer.parseInt(txtHarga.getText()));
        TR.setTempatDuduk(txtTempatDuduk.getText());
        
        String keteranganDiskon = "-";
       
        if(TR.getJumlah() >= 5){
            keteranganDiskon = "Diskon 20%";
        }
        else if(TR.getJumlah() >= 3){
            keteranganDiskon = "Diskon 10%";
        }
        else {
            keteranganDiskon = "Tidak ada diskon";
        }
        
        String metodePembayaran = cbMetodePembayaran.getSelectedItem().toString();  // Pastikan input metode valid
        int jumlahPembayaran = Integer.parseInt(txtJumlahPembayaran.getText());  // Jumlah uang yang dibayar
        
        String keteranganPembayaran =   TR.bayarTiket(metodePembayaran , jumlahPembayaran);
        
        
        DefaultTableModel model = (DefaultTableModel) tabelBioskop.getModel();
        model.addRow(new Object[]{
                TR.getJudulFilm(),
                TR.getJumlah(),
                "Rp." + TR.getHarga(),
                TR.getTempatDuduk(),
                "Rp." + TR.totalHarga(),
                "Biaya Tambahan 30%. Rp." + TR.BiayaTambahan(),
                keteranganDiskon + ". Rp." + TR.Diskon(),
                metodePembayaran,
                "Rp." + jumlahPembayaran,
                keteranganPembayaran ,
                TR.TanggalPembelian
        });
    }//GEN-LAST:event_btnSubmitActionPerformed

